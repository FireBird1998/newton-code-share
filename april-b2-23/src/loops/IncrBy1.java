package loops;

public class IncrBy1 {
    public static void main(String[] args) {
        int n = 4;
        System.out.println(n);
        n = n + 1; // add 1 to the n
        System.out.println(n);
        n++; // add 1 to the n
        System.out.println(n);
        n += 1;
        System.out.println(n);
    }
}
